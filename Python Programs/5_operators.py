# " + " --> addition
t1 = 10 + 10
print(t1)

#-------------------------
# " - " --> substration
t2 = 10 - 2
print(t2)

#-------------------------
# " * " --> multiplication
t3  = 10 * 2
print(t3)

#-------------------------
# " / " --> division
t4 = 10 / 4
print("Division :",t4)

#-------------------------
# " % " --> Reminder
t5 = 10 % 3
print(t5)

#-------------------------
# " // " --> floor division
t6 = 10 // 4
print("floor :",t6)

#-------------------------
# " ** " --> Exponential
t7 = 8 ** 1/3 # 8 ** 1/3 = 3 squarerootover 8 = 2.6666
print(t7)