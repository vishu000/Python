# The main concept of functions
# 1) Easy to develop
# 2) Easy to maintain
# 3) Provides more readability and clarity for program
# 4) Reduces length of the code
# 5) Supports Reusability
# 6) Reduces cost of maintaince

#------------------------------------------------------------
# CALLED Function
# def <functionname>(<arg1>,<arg2>,<arg3>,.....):
#       -----------------------------------------
#       -----------------------------------------
#       -----------------------------------------

#-----------------------------------------------------------
# CALLING Function
# <functionname>(<arg1>,<arg2>,<arg3>,.....)

# Types of arguments

# 1) Functions without arguments
#def <functionname>():
#       -----------------------------------------
#       -----------------------------------------
#       -----------------------------------------
# <functionname>()

#---------------------------------------------------------------
# 2) Functions with positional arguments
#def <functionname>(name1,name2):
#       -----------------------------------------
#       -----------------------------------------
#       -----------------------------------------
#<functionname>('vishnu','sai')

#---------------------------------------------------------------
# 3) Function with named arguments
#def <functionname>(name1,name2):
#       -----------------------------------------
#       -----------------------------------------
#       -----------------------------------------
#<functionname>(name1 = 'vishnu', name2 = 'sai')

#---------------------------------------------------------------
# 4) Function with Defalt arguments
#def <functionname>(name1 = 'vishnu',name2 = 'sai'):
#       -----------------------------------------
#       -----------------------------------------
#       -----------------------------------------
#<functionname>()

#---------------------------------------------------------------
# 5) Function with variable length arguments
#   (function that takes variable length arguments)
#def <functionname>(*args):
#       -----------------------------------------
#       -----------------------------------------
#       -----------------------------------------
#<functionname>(1,2,3)
#<functionname>(1,2,3,4)
#<functionname>(1,2,3,4,5,6)
